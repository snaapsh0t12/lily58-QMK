bool hid_started = false;
char msg[4][20];

void raw_hid_receive(unsigned char *data, uint8_t length) {
  hid_started = true;
  for (int i=0; i<19; i++)
    msg[data[0]][i] = data[i+1];
  msg[data[0]][19] = '\0';
}

